package week1.day1;

public class LearnConditionOperator {
	public static void main(String[] args) {
		int a=10;
		int b=5;
		{
			System.out.println(a+b);
			System.out.println(a-b);
			System.out.println(a/b);
			System.out.println(a*b);
			System.out.println(a%b);
			System.out.println(a<b);
			System.out.println(a>b);
			System.out.println(a!=b);
			
		}
		int c=10;
		{
			System.out.println(c++);
			System.out.println(c);
		}
		int d=12;
		{
			d/=2;
			System.out.println(d);
		}
	}

}
